# ansi escape sequences to create terminal colors

Default = '\033[0m'

Black = '\033[30m'
Red = '\033[31m'
Green = '\033[32m'
Yellow = '\033[33m'
Blue = '\033[34m'
Magenta = '\033[35m'
Cyan = '\033[36m'
White = '\033[37m'

Reset = '\033[0;0m'
Bold = '\033[1m'
Reverse = '\033[2m'

Blackbg = '\033[40m'
Redbg = '\033[41m'
Greenbg = '\033[42m'
Yellowbg = '\033[43m'
Bluebg = '\033[44m'
Magentabg = '\033[45m'
Cyanbg = '\033[46m'
Whitebg = '\033[47m'
